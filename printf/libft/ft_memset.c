#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*hum;
	int		i;

	i = 0;
	hum = s;
	while (n)
	{
		hum[i] = c;
		i++;
		n--;
	}
	return (s);
}
